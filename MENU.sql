	BIZNUM    NUMBER(20) REFERENCES MASTER_INFO(BIZNUM),
    CATEGORY  VARCHAR2(20)NOT NULL,  
    MENU_NAME VARCHAR(10) NOT NULL,
    PRICE     NUMBER(6)   NOT NULL,
    CNT       NUMBER(3)
);
